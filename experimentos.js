// Ejercicio 1: Conversor de grados celsius a fahrenheit

let fahrenheit = prompt("Ingresa grados en celsius");
fahrenheit = parseInt(fahrenheit);
fahrenheit = (fahrenheit * 9) / 5 + 32;
alert(fahrenheit + " grados fahrenheit");

